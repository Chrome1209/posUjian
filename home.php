    <p>Penyuplai Oksigen Terendam Air, Petambang Emas Ilegal Banyumas Belum Ditemukan</p>
    <ul>
        <li>PURWOKERTO, KOMPAS — Memasuki hari kelima, pencarian delapan petambang emas ilegal di Desa Pancurendang, Kecamatan Ajibarang, Banyumas, Jawa Tengah, belum membuahkan hasil. Padahal, alat penyuplai oksigen di lubang tambang sudah rusak sejak beberapa hari lalu.</li>
        <li>Sejauh ini, berbagai cara evakuasi sudah dilakukan. Selain menyiapkan 25 pompa penyedot air, tim pencari juga membendung Sungai Tajur dan Sungai Datar. Tujuannya, mencegah air menggenangi lubang.</li>
        <li>”Penyedotan sudah membuat ketinggian air turun sekitar 4 meter,” kata Kepala Subseksi Operasi dan Siaga di Badan SAR Nasional (Basarnas) Cilacap Priyo Prayudha Utama, Minggu (30/7/2023) siang.</li>
        <li>Akan tetapi, Priyo mengatakan belum bisa menggunakan ekskavator. Tim khawatir, pengoperasian alat itu bakal memicu bencana lain di sekitar lokasi kejadian.</li>
    </ul>
    <p>”Jika dipaksakan, takutnya malah likuefikasi,” ujar Priyo.</p>
